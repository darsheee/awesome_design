<script>
export default {
  transition: 'fade',
  scrollToTop: true,
  computed: {
    formattedDate() {
      const options = {
        year: 'numeric',
        month: 'long',
        day: 'numeric',
        timeZone: 'UTC',
      }
      return new Date(this.writing.date).toLocaleDateString('en-us', options)
    },
  },
  async asyncData({ $content, params }) {
    try {
      const writing = await $content('writing', params.slug).fetch()

      const [prev, next] = await $content('writing')
        .only(['date', 'slug'])
        .sortBy('date', 'asc')
        .surround(params.slug)
        .fetch()

      return {
        writing,
        prev,
        next,
      }
    } catch (err) {
      /* eslint-disable */
      console.debug('No Post:', err)
      /* eslint-enable */
      return false
    }
  },
  head() {
    if (!this.post) return
    return {
      titleTemplate: `${this.writing.title} â€“ %s`,
      meta: [
        {
          hid: 'description',
          name: 'description',
          content: this.writing.title,
        },
      ],
    }
  },
}
</script>

